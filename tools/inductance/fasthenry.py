def find_fasthenry(explicit: str | None) -> str | None:
    """Resolve a command-line fasthenry binary: explicit path, $FASTHENRY, or PATH."""
    for cand in (explicit, os.environ.get("FASTHENRY")):
        if cand and os.path.isfile(cand):
            return cand
    return shutil.which("fasthenry")


def find_solver(explicit: str | None) -> tuple[str | None, str | None]:
    """
    Locate a usable FastHenry. Returns (mode, target):
      ('cli', '/path/fasthenry')      -- a command-line binary (writes Zc.mat)
      ('com', 'FastHenry2.Document')  -- the FastFieldSolvers COM server (Windows)
      (None, None)                    -- nothing found
    The Windows FastFieldSolvers install ships only the GUI exe, which is also a
    COM automation server; we drive it headless via run_fasthenry_com.ps1.
    """
    cli = find_fasthenry(explicit)
    if cli:
        return ("cli", cli)
    # COM server? Look for its registered ProgID.
    try:
        r = subprocess.run(["reg", "query", r"HKCR\FastHenry2.Document"],
                           capture_output=True, text=True)
        if r.returncode == 0:
            return ("com", "FastHenry2.Document")
    except Exception:                            # noqa: BLE001
        pass
    return (None, None)


def run_via_com(inp_path: str, workdir: str,
                progid: str = "FastHenry2.Document",
                ps_script: str | None = None,
                max_retries: int = 4) -> list[tuple[float, float, float]]:
    """
    Solve a single deck `inp_path` via the FastHenry2 COM server and return
    [(freq_Hz, R_ohm, L_henry), ...] for the frequencies in the deck's `.freq`
    line. R is NaN if the server doesn't expose it.

    The COM server occasionally returns an empty result, and once an instance is
    wedged, recreating it in the SAME PowerShell process does not recover. So each
    retry is a FRESH PowerShell process (fresh COM apartment). The .ps1 writes the
    JSON only on a non-empty result; absence of the file means "retry".
    """
    here = os.path.dirname(os.path.abspath(__file__))
    ps_script = ps_script or os.path.join(here, "run_fasthenry_com.ps1")
    out_json = os.path.join(workdir, "fasthenry_com.json")
    inp_abs = os.path.abspath(inp_path)
    cmd = ["powershell", "-NoProfile", "-ExecutionPolicy", "Bypass",
           "-File", ps_script, "-Inp", inp_abs, "-Out", out_json, "-ProgId", progid]

    last = ""
    for attempt in range(1, max_retries + 1):
        if os.path.exists(out_json):
            os.remove(out_json)
        proc = subprocess.run(cmd, capture_output=True, text=True)
        last = proc.stdout + "\n" + proc.stderr
        if proc.returncode != 0:
            raise RuntimeError("FastHenry COM run failed (hard error):\n" + last)
        if os.path.isfile(out_json):
            break                                    # success: JSON written
        time.sleep(0.8)                              # empty result -> fresh-process retry
    else:
        raise RuntimeError(f"FastHenry returned an empty result after {max_retries} "
                           f"fresh-process attempts. Try a coarser mesh_pitch_mm and "
                           f"verify the closure.\n" + last)

    with open(out_json, encoding="utf-8-sig") as fh:
        data = json.load(fh)
    if isinstance(data, dict):
        data = [data]
    rows = []
    for d in data:
        R = d.get("R")
        rows.append((float(d["f"]), float(R) if R is not None else float("nan"),
                     float(d["L"])))
    if not rows:
        raise RuntimeError("FastHenry COM run returned no frequency points.\n" + last)
    rows.sort(key=lambda r: r[0])
    return rows


def sweep_frequencies(fmin: float, fmax: float, ndec: float) -> list[float]:
    """FastHenry's logarithmic sweep points: ndec per decade, fmin..fmax inclusive."""
    if fmax <= fmin:
        return [fmin]
    n = int(round(math.log10(fmax / fmin) * ndec))
    return [fmin * (10.0 ** (i / ndec)) for i in range(n + 1)]


def _rewrite_freq(deck_text: str, f: float) -> str:
    """Return the deck with its `.freq` line replaced by a single point at f."""
    out = []
    for line in deck_text.splitlines():
        if line.strip().lower().startswith(".freq"):
            out.append(f".freq fmin={f:.6g} fmax={f:.6g} ndec=1")
        else:
            out.append(line)
    return "\n".join(out) + "\n"


def run_sweep_per_freq(inp_path: str, freqs: list[float], workdir: str,
                       progid: str = "FastHenry2.Document",
                       progress=None) -> list[tuple[float, float, float]]:
    """
    Solve a frequency sweep as a SEQUENCE of single-frequency decks.

    The FastHenry2 COM server is unreliable on multi-frequency decks (returns an
    empty result for some meshes) but solves single-frequency decks dependably.
    So we rewrite the deck's `.freq` line per point and solve each separately,
    assembling L(f) ourselves. Slower (no shared setup) but robust.
    """
    with open(inp_path) as fh:
        deck = fh.read()
    rows = []
    for k, f in enumerate(freqs):
        single = _rewrite_freq(deck, f)
        tmp = os.path.join(workdir, "loop_pt.inp")
        with open(tmp, "w") as out:
            out.write(single)
        r = run_via_com(tmp, workdir, progid=progid)
        rows.extend(r)
        if progress:
            progress(k + 1, len(freqs), r[0])
    rows.sort(key=lambda r: r[0])
    return rows


def run_fasthenry(inp_path: str, binary: str, workdir: str) -> str:
    """Run fasthenry on inp_path inside workdir; return path to Zc.mat."""
    binary = os.path.abspath(binary)
    rel = os.path.relpath(os.path.abspath(inp_path), workdir)
    proc = subprocess.run([binary, rel], cwd=workdir,
                          capture_output=True, text=True)
    if proc.returncode != 0:
        raise RuntimeError(f"fasthenry failed (rc={proc.returncode}):\n"
                           f"{proc.stdout}\n{proc.stderr}")
    zc = os.path.join(workdir, "Zc.mat")
    if not os.path.isfile(zc):
        raise RuntimeError("fasthenry did not produce Zc.mat:\n" + proc.stdout)
    return zc


def parse_zc(zc_path: str) -> list[tuple[float, complex]]:
    """
    Parse FastHenry Zc.mat into [(freq_Hz, Z_ohm), ...] for a 1-port deck.
    Zc.mat blocks look like:
        Impedance matrix for frequency = 1e+06  1 x 1
        <real> <imag>
    """
    out: list[tuple[float, complex]] = []
    freq = None
    with open(zc_path) as fh:
        for line in fh:
            s = line.strip()
            mf = re.search(r"frequency\s*=\s*([0-9.eE+-]+)", s)
            if mf:
                freq = float(mf.group(1))
                continue
            if freq is not None:
                m = re.findall(r"[-+0-9.eE]+", s)
                if len(m) >= 2:
                    z = complex(float(m[0]), float(m[1]))
                    out.append((freq, z))
                    freq = None
    return out


def inductance_table(zc: list[tuple[float, complex]]) -> list[tuple[float, float, float]]:
    """Map [(f,Z)] -> [(f, R_ohm, L_henry)]; L=Im(Z)/(2*pi*f)."""
    rows = []
    for f, z in zc:
        L = z.imag / (2 * math.pi * f) if f > 0 else float("nan")
        rows.append((f, z.real, L))
    return rows
