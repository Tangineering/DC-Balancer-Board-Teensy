"""
Turn copper masks into a FastHenry model, run the solver, read back L(f).

Meshing model ("uniform Manhattan PEEC"):
  * Each filled cell of a CopperMask becomes a FastHenry node at the cell centre,
    at the layer's z height.
  * Orthogonally-adjacent filled cells are joined by a FastHenry segment (E line)
    of width = pitch, height = copper thickness. The resulting grid of bars is a
    discretised sheet; FastHenry then computes the full partial-inductance matrix
    among all bars, including mutual coupling and (via nwinc/nhinc filaments)
    skin/proximity effect.
  * Forward and return conductors live on different layers (different z). They are
    tied together by vertical VIA segments at the drilled via locations that land
    on both conductors -- this is the "far end / load" that closes the loop.
  * The measurement PORT is a `.external` between the forward node nearest
    terminal A and the return node nearest terminal B.

L(f) = Im(Z_port(f)) / (2*pi*f), with Z read from FastHenry's Zc.mat.

This module only *writes* and *parses*; it shells out to a `fasthenry` binary
whose path is configurable. Everything it writes goes under the caller-provided
output directory (kept inside tools/inductance/).
"""

from __future__ import annotations

import json
import math
import os
import re
import shutil
import subprocess
import time
from dataclasses import dataclass

import numpy as np

from geometry import CopperMask

MU0 = 4e-7 * math.pi
SIGMA_CU_SI = 5.8e7          # S/m, annealed copper


@dataclass
class Conductor:
    """One meshed conductor: its mask plus physical placement."""
    mask: CopperMask
    z: float                 # mm, height of the copper layer (segment centre plane)
    thickness: float         # mm, copper thickness
    tag: str                 # 'f' (forward) or 'r' (return); used in node names


@dataclass
class Via:
    x: float
    y: float
    diameter: float          # mm


def _cells(mask: CopperMask):
    """Yield (id, j, i, xc, yc) for each filled cell, id is a stable index."""
    js, is_ = np.nonzero(mask.mask)
    out = {}
    for k in range(len(js)):
        j, i = int(js[k]), int(is_[k])
        out[(j, i)] = k
    return js, is_, out


def build_inp(forward: Conductor,
              ret: Conductor,
              vias: list[Via],
              term_a: tuple[float, float],
              term_b: tuple[float, float],
              fmin: float, fmax: float, ndec: float,
              units: str = "mm",
              skin_ref_freq: float | None = None,
              max_filaments: int = 5,
              shorts: list[tuple[float, float]] | None = None) -> tuple[str, dict]:
    """
    Build a FastHenry input deck. Returns (text, meta) where meta records the
    segment count and the chosen port nodes for reporting.

    Loop closure happens by one of two mechanisms, depending on the physical case:
      * `vias`   : vertical copper segments tying forward<->return. Correct when
                   forward and return are the SAME net stitched across two layers.
      * `shorts` : ideal node merges (`.equiv`) between the nearest forward and
                   return nodes at each (x,y). Correct for a power->load->ground
                   loop, where the loop closes through an external component (the
                   load), NOT through a copper via. Place one short at the load.
    """
    lines: list[str] = []
    lines.append("* Auto-generated by gerber_inductance.py")
    lines.append("* Loop inductance: forward net + explicit return, swept L(f).")
    lines.append(f".units {units}")

    sigma = SIGMA_CU_SI / 1000.0 if units == "mm" else SIGMA_CU_SI  # S/mm vs S/m
    lines.append("")

    seg_count = 0
    node_count = 0
    port_nodes: dict[str, str] = {}

    # skin-depth-driven filament counts at the top of the sweep
    fref = skin_ref_freq if skin_ref_freq else fmax
    delta = _skin_depth(fref) * (1000.0 if units == "mm" else 1.0)   # in units

    for cond in (forward, ret):
        node_js, node_is, cell_id = _cells(cond.mask)
        p = cond.mask.pitch
        nhinc = _winc(cond.thickness, delta, max_filaments)
        nwinc = _winc(p, delta, max_filaments)

        lines.append(f"* ---- conductor '{cond.tag}'  z={cond.z} t={cond.thickness} "
                     f"cells={len(node_js)} ----")
        # nodes
        for k in range(len(node_js)):
            j, i = int(node_js[k]), int(node_is[k])
            xc, yc = cond.mask.cell_center(j, i)
            lines.append(f"N{cond.tag}{k} x={xc:.4f} y={yc:.4f} z={cond.z:.4f}")
            node_count += 1
        # segments: east and north neighbours
        cs = f"w={p:.4f} h={cond.thickness:.4f} sigma={sigma:.6g} "\
             f"nwinc={nwinc} nhinc={nhinc}"
        for (j, i), k in cell_id.items():
            for (dj, di) in ((0, 1), (1, 0)):
                nb = cell_id.get((j + dj, i + di))
                if nb is not None:
                    lines.append(f"E{cond.tag}{seg_count} N{cond.tag}{k} "
                                 f"N{cond.tag}{nb} {cs}")
                    seg_count += 1
        cond._cell_id = cell_id  # type: ignore[attr-defined]

    # ---- vias: vertical segments tying forward<->return ---------------------
    via_seg = 0
    fwd_cells = forward._cell_id            # type: ignore[attr-defined]
    ret_cells = ret._cell_id               # type: ignore[attr-defined]
    for v in vias:
        fj, fi = forward.mask.world_to_cell(v.x, v.y)
        rj, ri = ret.mask.world_to_cell(v.x, v.y)
        fk = fwd_cells.get((fj, fi))
        rk = ret_cells.get((rj, ri))
        if fk is None or rk is None:
            continue
        d = max(v.diameter, forward.mask.pitch)
        lines.append(f"Ev{via_seg} Nf{fk} Nr{rk} w={d:.4f} h={d:.4f} "
                     f"sigma={sigma:.6g} nwinc=1 nhinc=1")
        via_seg += 1

    # ---- port ---------------------------------------------------------------
    ka = _nearest_cell_id(forward, term_a)
    kb = _nearest_cell_id(ret, term_b)
    if ka is None or kb is None:
        raise ValueError("Could not place port: terminal A must land on the "
                         "forward net and terminal B on the return net.")
    na, nb = f"Nf{ka}", f"Nr{kb}"
    port_nodes = {"A": na, "B": nb}

    # ---- ideal-short closures (.equiv) at load/source ends ------------------
    n_short = 0
    for (sx, sy) in (shorts or []):
        fj, fi = forward.mask.world_to_cell(sx, sy)
        sk = fwd_cells.get((fj, fi))
        rj, ri = ret.mask.world_to_cell(sx, sy)
        rk = ret_cells.get((rj, ri))
        if sk is None:
            sk = _nearest_cell_id(forward, (sx, sy))
        if rk is None:
            rk = _nearest_cell_id(ret, (sx, sy))
        if sk is not None and rk is not None:
            lines.append(f".equiv Nf{sk} Nr{rk}")
            n_short += 1

    lines.append("")
    lines.append(f".external {na} {nb}")
    lines.append(f".freq fmin={fmin:.6g} fmax={fmax:.6g} ndec={ndec:.6g}")
    lines.append(".end")

    meta = {
        "segments": seg_count,
        "via_segments": via_seg,
        "shorts": n_short,
        "nodes": node_count,
        "port_nodes": port_nodes,
        "skin_depth_mm": delta,
    }
    return "\n".join(lines) + "\n", meta


def _nearest_cell_id(cond: Conductor, pt: tuple[float, float]):
    cell_id = cond._cell_id            # type: ignore[attr-defined]
    j0, i0 = cond.mask.world_to_cell(*pt)
    if (j0, i0) in cell_id:
        return cell_id[(j0, i0)]
    best, bestd = None, 1e18
    for (j, i), k in cell_id.items():
        d = (j - j0) ** 2 + (i - i0) ** 2
        if d < bestd:
            bestd, best = d, k
    return best


def _skin_depth(freq: float) -> float:
    """Copper skin depth (metres) at `freq` Hz. DC -> large but capped."""
    if freq <= 0:
        return 1.0
    return math.sqrt(2.0 / (2 * math.pi * freq * MU0 * SIGMA_CU_SI))


def _winc(dim: float, delta: float, cap: int) -> int:
    """Filament count to resolve skin effect across `dim` given skin depth delta."""
    if delta <= 0:
        return 1
    n = int(math.ceil(dim / max(delta, 1e-9)))
    return max(1, min(cap, n))


